import Foundation

// Little bit more advanced but greeting would be considered an object with the type String with the assigned value of "Hello, playground"
var greeting = "Hello, playground"

// String is "regular text"
let myFirstItem = "Hello, world"

// Reference previously created objects by assigning their value to a new object
let myTitle = myFirstItem

// Boolean (aka Bool) which is true or false
let mySecondItem: Bool = true
let myThirdItem: Bool = false
// Do not do let myFourthItem: String = "true"

// Swift is a type-safe language
let myFifthItem: Bool = true
let mySixthItem: String = "Hello, world!"
let mySeventhItem = true

// Date
let myFirstDate: Date = .distantPast

// Numbers: Int, Double, CGFloat & more
let myFirstNumber: Int = 1
let mySecondNumber: Double = 1.0
let myThirdNumber: CGFloat = 1.0
